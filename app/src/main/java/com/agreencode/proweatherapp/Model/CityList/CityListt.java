package com.agreencode.proweatherapp.Model.CityList;

import com.agreencode.proweatherapp.Model.geographicCoordinates.Coord;

public class CityListt {
    private int id;
    private String name,country;
    private Coord coord;

    public CityListt(int id, String name, String country) {
        this.id = id;
        this.name = name;
        this.country = country;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public Coord getCoord() {
        return coord;
    }

    public void setCoord(Coord coord) {
        this.coord = coord;
    }
}
